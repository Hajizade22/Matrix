package Lesson_26;

public class Person {
    String name;
    int price;
    String category;

    public Person(String name, int price, String category) {
        this.name = name;
        this.price = price;
        this.category = category;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    @Override
    public String toString() {
        return "Name : '" + name + '\'' +
                ", Price  : " + price +
                ", Category : " + category + '\'';
    }
}
